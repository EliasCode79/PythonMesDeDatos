# the assignment of variables in python is done in the following way


is_name = 'elias'
is_integer = 20
is_flotante = 2.2
is_boolean = True

print(is_name, is_integer, is_flotante, is_boolean)

# if we want to know the data type of a variable we use the type() method, which will tell us the data type 
print(type(is_name))
print(type(is_integer))
print(type(is_flotante))
print(type(is_boolean))


# we can concatenate the varibals, placing "f" before the apostrophe signs
age = 24
print(f'My name is {is_name} and I am {age} years old ')